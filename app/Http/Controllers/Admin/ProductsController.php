    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $category = Category::whereHas('products')->get();
        // dd($category);
        $products = Product::query()
            ->with('category')
            ->when($request->category, fn ($q, $v) => $q->whereBelongsTo(Category::where('slug', $v)->first()))
            ->select('id', 'price', 'slug', 'name', 'picture', 'category_id', 'status')
            ->paginate(12)
            ->withQueryString();

        // return ProductResource::collection($products);
        // return ProductResource::collection($products);
        return inertia('Dashboard/Products/Index', [
            'products' => ProductResource::collection($products),
            'category' => $category
        ]);
    }

